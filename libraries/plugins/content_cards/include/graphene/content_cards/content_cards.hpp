#pragma once

#include <graphene/app/plugin.hpp>
#include <graphene/chain/database.hpp>

namespace graphene { namespace content_cards {
using namespace chain;

//
// Plugins should #define their SPACE_ID's so plugins with
// conflicting SPACE_ID assignments can be compiled into the
// same binary (by simply re-assigning some of the conflicting #defined
// SPACE_ID's in a build script).
//
// Assignment of SPACE_ID's cannot be done at run-time because
// various template automagic depends on them being known at compile
// time.
//
#ifndef CONTENT_CARDS_SPACE_ID
#define CONTENT_CARDS_SPACE_ID 8
#endif


namespace detail
{
    class content_cards_impl;
}

class content_cards : public graphene::app::plugin
{
   public:
      content_cards();
      virtual ~content_cards();

      std::string plugin_name()const override;
      std::string plugin_description()const override;
      virtual void plugin_set_program_options(
         boost::program_options::options_description& cli,
         boost::program_options::options_description& cfg) override;
      virtual void plugin_initialize(const boost::program_options::variables_map& options) override;
      virtual void plugin_startup() override;

      friend class detail::content_cards_impl;
      std::unique_ptr<detail::content_cards_impl> my;
};

} } //graphene::template
